def convert_to_uppercase(string):
    """функция, которая принимает на вход строку и возвращает ее со всеми заглавными буквами."""
    return string.upper()

def capitalize_first_letters(string):
    """функцию, которая делает заглавными первые буквы каждого слова в строке,
    поступившей на вход функции."""
    return string.title()